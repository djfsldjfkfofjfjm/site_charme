#!/bin/bash

# Скрипт для сброса локальных изменений и возврата к версии на GitHub
# Использование: ./reset.sh

echo "ВНИМАНИЕ: Этот скрипт отменит ВСЕ локальные изменения и вернет файлы к состоянию на GitHub."
echo "Все несохраненные изменения будут утеряны безвозвратно!"
echo ""

read -p "Вы уверены, что хотите продолжить? (y/n): " confirm
if [ "$confirm" != "y" ]; then
  echo "Операция отменена."
  exit 1
fi

echo "Получение последней версии с GitHub..."
git fetch origin

echo "Сброс локальных изменений..."
git reset --hard origin/main

echo ""
echo "Сброс выполнен успешно! Все файлы теперь соответствуют версии на GitHub."